#!/bin/bash

# Проверяем, что указан аргумент с расширением
if [ -z "$1" ]; then
  echo "Укажите расширение файла в качестве аргумента."
  exit 1
fi

# Получаем текущую дату и время для имени архива
ARCHIVE_NAME="files_$(date +%Y%m%d%H%M%S).tar"

# Ищем все файлы с указанным расширением в текущем каталоге и архивируем их
find . -type f -name "*.$1" -exec tar -rf "$ARCHIVE_NAME" {} +

# Проверяем, что архив создан
if [ -f "$ARCHIVE_NAME" ]; then
  echo "Архив $ARCHIVE_NAME успешно создан."
else
  echo "Файлы с расширением $1 не найдены."
fi
